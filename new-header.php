        let hamburger_link = document.querySelectorAll('.hamburger-link');

        for(let i = 0; i < hamburger_link.length; i++) {
            hamburger_link[i].addEventListener('click', () => {
                $(hamburger_link[i]).next().closest('.mobile-dropdown').toggleClass('show-menu');
                $(hamburger_link[i]).toggleClass('focus');
            })
        }

        let first_child = document.querySelectorAll('.dropdown-in-dropdown');

        for(let i = 0; i < first_child.length; i++) {
            first_child[i].addEventListener('click', () => {
                $(first_child[i]).next().closest('.child-dropdown').toggleClass('show-menu')
            })
